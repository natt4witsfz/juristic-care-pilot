function fieldChange(label, before, after) {
  const oldValue = before === undefined || before === null || before === "" ? "-" : before;
  const newValue = after === undefined || after === null || after === "" ? "-" : after;
  return `${label}: ${oldValue} → ${newValue}`;
}
function booleanChange(label, before, after) {
  return `${label}: ${before ? "ON" : "OFF"} → ${after ? "ON" : "OFF"}`;
}
async function saveTeamMember(userId) {
  if (!canManageTeam()) return;
  const user = getUser(userId);
  if (!user || user.role === "admin") return;
  const parts = splitFullName(user);
  const next = {
    firstName: (teamCardValue(userId, "data-team-first")?.value || parts.firstName).trim(),
    lastName: (teamCardValue(userId, "data-team-last")?.value || parts.lastName).trim(),
    nickName: (teamCardValue(userId, "data-team-nick")?.value || "").trim(),
    position: (teamCardValue(userId, "data-team-position")?.value || "").trim(),
    department: teamCardValue(userId, "data-team-department")?.value || user.department,
    assignL1: !!teamCardValue(userId, "data-toggle-l1")?.checked,
    assignL2: !!teamCardValue(userId, "data-toggle-l2")?.checked
  };
  if (currentUser.role === "admin") {
    next.room = (teamCardValue(userId, "data-admin-user-id")?.value || user.room || "").trim().toUpperCase();
    next.password = (teamCardValue(userId, "data-admin-password")?.value || user.password || "").trim();
  }
  const photoInput = teamCardValue(userId, "data-team-photo");
  const changes = [];
  if ((user.firstName || parts.firstName) !== next.firstName) changes.push(fieldChange("firstName", user.firstName || parts.firstName, next.firstName));
  if ((user.lastName || parts.lastName) !== next.lastName) changes.push(fieldChange("lastName", user.lastName || parts.lastName, next.lastName));
  if ((user.nickName || "") !== next.nickName) changes.push(fieldChange("nickname", user.nickName, next.nickName));
  if ((user.position || "") !== next.position) changes.push(fieldChange("position", user.position, next.position));
  if ((user.department || "") !== next.department) changes.push(fieldChange("department", departmentLabel(user.department), departmentLabel(next.department)));
  if (!!user.assignL1 !== next.assignL1) changes.push(booleanChange("assignL1", user.assignL1, next.assignL1));
  if (!!user.assignL2 !== next.assignL2) changes.push(booleanChange("assignL2", user.assignL2, next.assignL2));
  if (currentUser.role === "admin" && (user.room || "") !== (next.room || "")) changes.push(fieldChange("loginId", user.room, next.room));
  if (currentUser.role === "admin" && (user.password || "") !== (next.password || "")) changes.push("password: changed");
  if (photoInput?.files?.[0]) {
    next.profileImage = await fileToDataUrl(photoInput.files[0]);
    changes.push("profileImage: changed");
  }
  if (!changes.length) return showToast(currentLang === "th" ? "ยังไม่มีข้อมูลที่เปลี่ยนแปลง" : "No changes to save");
  persistUserFields(user, next);
  addLog("TEAM_UPDATED", `${getUserName(user)} updated by ${getUserName(currentUser)} — ${changes.join("; ")}`);
  renderTeam();
  showToast(currentLang === "th" ? "บันทึกข้อมูลพนักงานแล้ว" : "Team member saved");
}
