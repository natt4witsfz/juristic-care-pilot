function parseOccupants(value) {
  return String(value || "").split(";").map(item => item.trim()).filter(Boolean).map(item => {
    const [firstName = "", lastName = "", nickName = "", phone = "", status = "resident", isCurrent = "no"] = item.split("|");
    return { firstName, lastName, nickName, phone, status: status || "resident", isCurrentResident: ["yes", "true", "1"].includes(String(isCurrent).toLowerCase()) };
  });
}
function parseCars(value) {
  return String(value || "").split(";").map(item => item.trim()).filter(Boolean).map(item => {
    const [prefix = "", number = "", province = "", brand = "", model = "", color = ""] = item.split("|");
    return { prefix, number, province, brand, model, color };
  });
}
async function importResidentsCsv(file) {
  const text = await file.text();
  const rows = parseCsv(text.replace(/^\ufeff/, ""));
  const headers = rows.shift()?.map(header => header.trim()) || [];
  if (!headers.includes("room_no_1") || !headers.includes("room_no_2")) return showToast("CSV ต้องมีคอลัมน์ room_no_1 และ room_no_2");
  let imported = 0;
  rows.forEach(row => {
    const record = Object.fromEntries(headers.map((header, index) => [header, row[index] || ""]));
    const roomNo1 = record.room_no_1.trim();
    const roomNo2 = record.room_no_2.trim();
    if (!roomNo1 && !roomNo2) return;
    const existing = residentRooms.find(room => room.roomNo1 === roomNo1 || room.roomNo2 === roomNo2);
    const next = {
      id: existing?.id || `room-${Date.now()}-${imported}`,
      roomNo1,
      roomNo2,
      building: record.building.trim(),
      commonFeeStatus: record.common_fee_status === "paid" ? "paid" : "unpaid",
      owner: {
        firstName: record.owner_first_name || "",
        lastName: record.owner_last_name || "",
        nickName: record.owner_nickname || "",
        phone: record.owner_phone || "",
        status: "owner",
        isCurrentResident: ["yes", "true", "1"].includes(String(record.owner_is_current || record.owner_is_primary || "yes").toLowerCase())
      },
      occupants: parseOccupants(record.occupants),
      cars: parseCars(record.cars),
      personOrder: [record.primary_person_key || "owner", "owner", ...parseOccupants(record.occupants).map((_, index) => `occupant-${index}`)].filter((value, index, array) => value && array.indexOf(value) === index)
    };
    if (existing) Object.assign(existing, next);
    else if (residentRooms.length < 882) residentRooms.push(next);
    imported++;
  });
  saveResidentRooms();
  addLog("RESIDENTS_IMPORTED", `${imported} resident room rows imported by ${getUserName(currentUser)}`);
  renderResidents();
  showToast(`Import CSV แล้ว ${imported} รายการ`);
}
