function readFileAsDataUrl(file) {
  return new Promise(resolve => {
    const reader = new FileReader();
    reader.onload = () => resolve(reader.result);
    reader.readAsDataURL(file);
  });
}
function loadImage(src) {
  return new Promise((resolve, reject) => {
    const image = new Image();
    image.onload = () => resolve(image);
    image.onerror = reject;
    image.src = src;
  });
}
function imageStampText() {
  const userName = currentUser ? getUserName(currentUser) : "System";
  const stampTime = new Date().toLocaleString(currentLang === "th" ? "th-TH" : "en-US");
  return `${userName} · ${stampTime}`;
}
async function fileToDataUrl(file) {
  const dataUrl = await readFileAsDataUrl(file);
  if (!file?.type?.startsWith("image/")) return dataUrl;
  try {
    const image = await loadImage(dataUrl);
    const canvas = document.createElement("canvas");
    const maxSide = 1600;
    const scale = Math.min(1, maxSide / Math.max(image.naturalWidth || image.width, image.naturalHeight || image.height));
    canvas.width = Math.max(1, Math.round((image.naturalWidth || image.width) * scale));
    canvas.height = Math.max(1, Math.round((image.naturalHeight || image.height) * scale));
    const ctx = canvas.getContext("2d");
    ctx.drawImage(image, 0, 0, canvas.width, canvas.height);
    const stamp = imageStampText();
    const fontSize = Math.max(14, Math.round(canvas.width * 0.024));
    const padding = Math.max(10, Math.round(fontSize * 0.7));
    ctx.font = `700 ${fontSize}px "Anuphan", "IBM Plex Sans Thai", Arial, sans-serif`;
    const textWidth = ctx.measureText(stamp).width;
    const boxWidth = Math.min(canvas.width, textWidth + padding * 2);
    const boxHeight = Math.round(fontSize + padding * 1.5);
    ctx.fillStyle = "rgba(7, 28, 22, 0.68)";
    ctx.fillRect(0, canvas.height - boxHeight, boxWidth, boxHeight);
    ctx.fillStyle = "#ffffff";
    ctx.fillText(stamp, padding, canvas.height - padding);
    return canvas.toDataURL("image/jpeg", 0.88);
  } catch (error) {
    console.warn("Image stamp failed, using original file", error);
    return dataUrl;
  }
}
