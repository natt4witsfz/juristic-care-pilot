function csvCell(value) {
  return `"${String(value ?? "").replaceAll('"', '""')}"`;
}
function csvLine(values) {
  return values.map(csvCell).join(",");
}
function parseCsv(text) {
  const rows = [];
  let row = [], cell = "", quoted = false;
  for (let i = 0; i < text.length; i++) {
    const char = text[i];
    const next = text[i + 1];
    if (quoted) {
      if (char === '"' && next === '"') { cell += '"'; i++; }
      else if (char === '"') quoted = false;
      else cell += char;
    } else if (char === '"') quoted = true;
    else if (char === ",") { row.push(cell); cell = ""; }
    else if (char === "\n") { row.push(cell); rows.push(row); row = []; cell = ""; }
    else if (char !== "\r") cell += char;
  }
  row.push(cell);
  if (row.some(value => value !== "")) rows.push(row);
  return rows;
}
function occupantToCsv(person) {
  return [person.firstName, person.lastName, person.nickName, person.phone, person.status, person.isCurrentResident ?? person.isPrimary ? "yes" : "no"].map(value => String(value || "").replaceAll("|", "/")).join("|");
}
function carToCsv(car) {
  return [car.prefix, car.number, car.province, car.brand, car.model, car.color].map(value => String(value || "").replaceAll("|", "/")).join("|");
}
