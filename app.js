function getViewJobs(view = currentView) {
  if (!currentUser) return [];
  if (view === "commonWork") return jobs.filter(j => {
    const job = normalizeJob(j);
    return canViewJob(currentUser, job) && job.mainCategory === "common";
  });
  if (view === "commonWork") return jobs.filter(j => {
    const job = normalizeJob(j);
    return canViewJob(currentUser, job) && (job.areaType === "common" || job.sharePublic || String(job.roomNo || job.room || "").includes("ส่วนกลาง") || String(job.roomNo || job.room || "").toLowerCase().includes("common"));
  });
  if (view === "pool") return jobs.filter(j => currentUser.role !== "resident" && j.raw);
  if (view === "juristicTeam") return jobs.filter(j => currentUser.role !== "resident" && !j.raw && (j.source === "WebApp" || j.source === "Assigned" || j.assignedBy || j.createdBy));
  if (view === "myjobs") return jobs.filter(j => normalizeJob(j).assignee === currentUser.id);
  if (view === "technicianJobs") return jobs.filter(j => {
    if (currentUser.role === "resident") return false;
    const assignee = getUser(normalizeJob(j).assignee);
    return inferDepartment(assignee || {}) === "technician";
  });
  if (view === "housekeepingJobs") return jobs.filter(j => {
    if (currentUser.role === "resident") return false;
    const assignee = getUser(normalizeJob(j).assignee);
    return inferDepartment(assignee || {}) === "housekeeping";
  });
  if (view === "roomjobs") return jobs.filter(j => getText(j.roomNo || j.room) === currentUser.room);
  if (view === "dashboard") return jobs.filter(j => canViewJob(currentUser, normalizeJob(j)));
  return jobs.filter(j => canViewJob(currentUser, normalizeJob(j)));
}
