function validateStatusUpdate(job, payload) {
  const errors = [];
  const status = payload.status;
  const requiresAttachment = !["received", "rejected"].includes(status);
  if (requiresAttachment && !payload.attachments?.length) errors.push("กรุณาแนบรูปอย่างน้อย 1 รูป");
  if ((payload.attachments?.length || 0) > 3) errors.push("แนบรูปได้สูงสุด 3 รูป");
  if (status === "pending_inspection" && !payload.inspectionDate) errors.push("กรุณาเลือกวันที่เข้าตรวจสอบ");
  if ((status === "repaired_follow_up" || status === "temporary_waiting_parts") && !payload.nextUpdateDate) errors.push("กรุณาเลือกวันที่อัปเดต/ติดตามครั้งถัดไป");
  if (status === "inspected_waiting_repair") {
    if (!payload.updateDate) errors.push("กรุณาเลือกวันที่ตรวจสอบ / วันที่อัปเดต");
    if (!payload.cause) errors.push("กรุณากรอกสาเหตุ");
    if (!payload.solution) errors.push("กรุณากรอกแนวทางแก้ไข");
  }
  if (status === "completed") {
    if (payload.noPinAvailable) {
      if (!payload.noPinReason) errors.push("กรุณากรอกเหตุผลที่ไม่มี PIN");
      if (!payload.attachments?.length) errors.push("กรุณาแนบรูปอย่างน้อย 1 รูปเมื่อไม่มี PIN");
    } else if (payload.closePin !== job.closePin) errors.push("PIN ไม่ถูกต้อง");
  }
  if (payload.attachments?.some(file => file.size && file.size > 5 * 1024 * 1024)) errors.push("รูปภาพต้องมีขนาดไม่เกิน 5MB ต่อไฟล์");
  return errors;
}
function updateJobStatus(jobId, payload) {
  const job = normalizeJob(jobs.find(item => item.id === jobId));
  if (!job) return { errors: ["ไม่พบงาน"] };
  const errors = validateStatusUpdate(job, payload);
  if (errors.length) return { errors };
  const fromStatus = job.status;
  const now = new Date().toISOString();
  if (payload.status === "completed" && payload.noPinAvailable) {
    job.status = "pending_inspection";
    job.subStatus = "waiting_owner_or_admin_verification";
    job.noPinAvailable = true;
    job.noPinReason = payload.noPinReason;
    job.pendingVerificationAt = now;
    job.completedAt = "";
  } else {
    job.status = payload.status;
    job.subStatus = "";
    if (payload.status === "completed") {
      job.completedAt = now;
      job.completedWorkDate = payload.completedWorkDate || now.slice(0, 10);
    }
  }
  if (payload.status === "received") job.acceptedAt = now;
  if (payload.status === "pending_inspection" && payload.inspectionDate) job.inspectionDate = payload.inspectionDate;
  if ((payload.status === "repaired_follow_up" || payload.status === "temporary_waiting_parts") && payload.nextUpdateDate) job.nextUpdateDate = payload.nextUpdateDate;
  if (payload.status === "inspected_waiting_repair") {
    job.inspectionDate = payload.updateDate;
    job.cause = payload.cause;
    job.solution = payload.solution;
  }
  job.note = payload.note || job.note || "";
  if (payload.attachments?.length) job.attachments = [...(job.attachments || []), ...payload.attachments];
  job.statusUpdates = job.statusUpdates || [];
  job.statusUpdates.unshift({ at: now, by: currentUser.id, byName: getUserName(currentUser), ...payload, attachmentsCount: payload.attachments?.length || 0 });
  addJobTimeline(job.id, {
    action: payload.noPinAvailable ? "complete_without_pin" : "status_update",
    fromStatus,
    toStatus: job.status,
    message: payload.noPinAvailable
      ? `ช่างแจ้งว่าแก้ไขงานเสร็จแล้ว แต่ไม่สามารถขอ PIN ได้ เนื่องจาก ${payload.noPinReason}`
      : `เปลี่ยนสถานะเป็น ${statusLabel(job.status)} โดย ${getUserName(currentUser)}${payload.cause ? ` สาเหตุ: ${payload.cause} แนวทางแก้ไข: ${payload.solution}` : ""}`,
    data: { subStatus: job.subStatus, noPinAvailable: job.noPinAvailable, attachmentsCount: payload.attachments?.length || 0 }
  });
  saveJobs();
  addLog("JOB_UPDATED", `${job.id} updated to ${statusLabel(job.status, job.subStatus)}`);
  return { job, errors: [] };
}
